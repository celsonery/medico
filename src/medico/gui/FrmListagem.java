    private void btnPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrintActionPerformed
        try {
            JasperReport report = JasperCompileManager.compileReport("reports/ListagemPacientes.jrxml");
            JasperPrint print   = JasperFillManager.fillReport(report, null, new JRBeanCollectionDataSource(pacList));
            
            JasperViewer view = new JasperViewer(print, false);
            view.setVisible(true);            
        } catch (JRException ex) {
            Logger.getLogger(FrmListagem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnPrintActionPerformed

